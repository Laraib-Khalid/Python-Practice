# Declaring two integer variables
n = 295
m = 19

# -------------------------------
# Arithmetic Operations in Python
# -------------------------------

# Addition (+) → adds two numbers
print("The value of", n, "+", m, "is", n + m)    # 295 + 19 = 314

# Subtraction (-) → subtracts the right operand from the left
print("The value of", n, "-", m, "is", n - m)    # 295 - 19 = 276

# Multiplication (*) → multiplies both numbers
print("The value of", n, "*", m, "is", n * m)    # 295 * 19 = 5605

# Division (/) → divides left operand by right and returns a float value
print("The value of", n, "/", m, "is", n / m)    # 295 / 19 = 15.526315789473685

# Modulus (%) → returns the remainder after division
print("The value of", n, "%", m, "is", n % m)    # 295 % 19 = 10  (remainder when 295 is divided by 19)

# Exponentiation (**) → raises left operand to the power of right operand
print("The value of", n, "**", m, "is", n ** m)  # 295^19 (very large number — exponential result)

# Floor Division (//) → divides and returns the integer part only (no decimal)
print("The value of", n, "//", m, "is", n // m)  # 295 // 19 = 15  (decimal part is discarded)

